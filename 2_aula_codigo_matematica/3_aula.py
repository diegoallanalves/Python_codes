# Python permite fazer inúmeras funções matemáticas que eventualmente necessitamos usar no mercado de trabalho, veja:

# Primeiro vamos criar uma variavel chamada resultado:

Digite_primeiro_valor = input("Digite primeiro valor:") #Criada a primaeira variavel
Digite_primeiro_valor = int(Digite_primeiro_valor) #Antes temos que converter o valor indicado para o formato numérico: integer
Digite_segundo_valor = input("Digite segundo valor:") #Criada a segunda variavel
Digite_segundo_valor = int(Digite_segundo_valor) #Antes temos que converter o valor indicado para o formato numérico: integer
print(Digite_primeiro_valor * Digite_segundo_valor) #Na função 'print', indicaremos as condições Matematicas(+,-,*,/).


